package cn.kevin.exception;

public class IdIsNullException extends Exception {

	public IdIsNullException() {
	}

	public IdIsNullException(String arg0) {
		super(arg0);
	}

	public IdIsNullException(Throwable arg0) {
		super(arg0);
	}

	public IdIsNullException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IdIsNullException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super();
	}

}
